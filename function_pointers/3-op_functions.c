#include "3-calc.h"

/**
 * op_add - function to sum two integers
 *
 * @a : integer a
 *
 * @b : integer b
 *
 * Return: the result of the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to substract two integers
 *
 * @a : integer a
 *
 * @b : integer b
 *
 * Return: the result of the substraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiplicate two integers
 *
 * @a : integer a
 *
 * @b : integer b
 *
 * Return: the product of the multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two integers
 *
 * @a : integer a
 *
 * @b : integer b
 *
 * Return: the result of the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to return the module of two integers
 *
 * @a : integer a
 *
 * @b : integer b
 *
 * Return: the result of the module between a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
